class HomeScreen extends StatefulWidget {
  @override
  _HomeScreenState createState() => _HomeScreenState();
}

class _HomeScreenState extends State<HomeScreen> {
  final FirestoreService _firestoreService = FirestoreService();
  List<Group> _groups = [];
  User? user;
  String email = 'No email';

  @override
  void initState() {
    super.initState();
    _loadGroups();
    user = FirebaseAuth.instance.currentUser;
    email = user?.email ?? 'No email';
  }

  Future<void> _loadGroups() async {
    QuerySnapshot snapshot =
        await FirebaseFirestore.instance.collection('groups').get();
    setState(() {
      _groups = snapshot.docs
          .map((doc) => Group.fromMap(doc.data() as Map<String, dynamic>))
          .toList();
    });
  }

  Future<void> _createNewGroup() async {
    TextEditingController nameController = TextEditingController();
    TextEditingController descriptionController = TextEditingController();

    showDialog(
      context: context,
      builder: (BuildContext context) {
        return AlertDialog(
          title: Text('Create New Group'),
          content: Column(
            mainAxisSize: MainAxisSize.min,
            children: [
              TextField(
                controller: nameController,
                decoration: InputDecoration(labelText: 'Group Name'),
              ),
              TextField(
                controller: descriptionController,
                decoration: InputDecoration(labelText: 'Description'),
              ),
            ],
          ),
          actions: [
            TextButton(
              onPressed: () async {
                String id = DateTime.now().millisecondsSinceEpoch.toString();
                Group newGroup = Group(
                  id: id,
                  name: nameController.text,
                  description: descriptionController.text,
                  image: 'assets/images/app_logo.png',
                  ownerId: 'currentUserId',
                  // Replace with actual user ID
                  students: [],
                );
                await _firestoreService.createGroup(newGroup);
                _loadGroups();
                Navigator.of(context).pop();
              },
              child: Text('Create'),
            ),
            TextButton(
              onPressed: () {
                Navigator.of(context).pop();
              },
              child: Text('Cancel'),
            ),
          ],
        );
      },
    );
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        leading: Builder(
          builder: (BuildContext context) {
            return IconButton(
              icon: const Icon(Icons.menu),
              onPressed: () {
                Scaffold.of(context).openDrawer();
              },
            );
          },
        ),
        title: Row(
          children: [
            TextButton(
              child: Text('Home', style: TextStyle(color: Colors.black)),
              onPressed: () {},
            ),
            TextButton(
              child: Text('Projects', style: TextStyle(color: Colors.grey)),
              onPressed: () {},
            ),
            TextButton(
              child: Text('Planning', style: TextStyle(color: Colors.grey)),
              onPressed: () {},
            ),
            TextButton(
              child: Text('Reports', style: TextStyle(color: Colors.grey)),
              onPressed: () {},
            ),
            TextButton(
              child: Text('Calendar', style: TextStyle(color: Colors.grey)),
              onPressed: () {},
            ),
            TextButton(
              child: Text('People', style: TextStyle(color: Colors.grey)),
              onPressed: () {},
            ),
          ],
        ),
        actions: [
          Container(
            width: 200,
            margin: EdgeInsets.symmetric(vertical: 8),
            decoration: BoxDecoration(
              color: Colors.white,
              borderRadius: BorderRadius.circular(20),
            ),
            child: TextField(
              decoration: InputDecoration(
                hintText: 'Search Here',
                prefixIcon: Icon(Icons.search),
                border: InputBorder.none,
              ),
            ),
          ),
          IconButton(icon: Icon(Icons.chat_bubble_outline), onPressed: () {}),
          IconButton(icon: Icon(Icons.notifications_none), onPressed: () {}),
          Padding(
            padding: const EdgeInsets.symmetric(horizontal: 8.0),
            child: Column(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
                CircleAvatar(
                  radius: 15,
                  backgroundImage: AssetImage('assets/images/app_logo.png'),
                ),
                SizedBox(height: 2),
                Text(
                  email,
                  style: TextStyle(color: Colors.black, fontSize: 10),
                ),
              ],
            ),
          ),
        ],
      ),
      drawer: CustomDrawer(),
      body: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const Padding(
            padding: EdgeInsets.all(16.0),
            child: Text(
              'YOUR TEAMS:',
              style: TextStyle(
                color: Colors.black,
                fontSize: 20,
                fontWeight: FontWeight.bold,
                decoration: TextDecoration.underline,
                decorationStyle: TextDecorationStyle.double,
              ),
              //khung chá»¯
              textAlign: TextAlign.left,
            ),
          ),
          Expanded(
            child: GridView.count(
              crossAxisCount: 3,
              padding: const EdgeInsets.fromLTRB(100.0, 20.0, 100.0, 8.0),
              childAspectRatio: 2.5,
              mainAxisSpacing: 8.0,
              crossAxisSpacing: 8.0,
              children: _groups.map((group) {
                return GestureDetector(
                  onTap: () {
                    Navigator.push(
                      context,
                      MaterialPageRoute(
                        builder: (context) =>
                            TaskDetailScreen(groupId: group.id),
                      ),
                    );
                  },
                  child: TeamCard(group: group),
                );
              }).toList(),
            ),
          ),
        ],
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _createNewGroup,
        child: Icon(Icons.add),
      ),
    );
  }
}

